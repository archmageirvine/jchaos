package irvine.heraldry;

/**
 * Ordinaries.
 *
 * @author Sean A. Irvine
 */
public enum Ordinary {
  /** Annulet. Ring. */
  ANNULET,
  /** Barry. Parallel horizontal bars. */
  BARRY,
  /** Bars. Two parallel horizontal bars. */
  BARS,
  /** Bend. Diagonal band. */
  BEND,
  /** Bend sinister. Reverse diagonal band. */
  BEND_SINISTER,
  /** Bendlets. Two bends. */
  BENDLETS,
  /** Bendlets sinister. Reverse twin diagonal bends. */
  BENDLETS_SINISTER,
  /** Bendy. Diagonal bars. */
  BENDY,
  /** Billets. Three small rectangles. */
  BILLETS,
  /** Bordure. Sub-shield. */
  BORDURE,
  /** Canton. Top-left quarter. */
  CANTON,
  /** Chevronels. Little chevrons at the bottom. */
  CHEVRONELS,
  /** Chief. One horizontal band at the top. */
  CHIEF,
  /** Cross. */
  CROSS,
  /** Fess. Straight horizontal line. */
  FESS,
  /** Flanches. Curved chords on sides. */
  FLANCHES,
  /** Fusil. Shield in a shield. */
  FUSIL,
  /** Gyronny. Wedges. */
  GYRONNY,
  /** Inescutcheon. Sub-shield. */
  INESCUTCHEON,
  /** None. No division. */
  NONE,
  /** Orle. Shield in a shield in a shield. */
  ORLE,
  /** Pale. Vertical band. */
  PALE,
  /** Pallets. Parallel vertical bars. */
  PALLETS,
  /** Paly. Vertical bars. */
  PALY,
  /** Party per bend. Divided in half diagonally. */
  PARTY_PER_BEND,
  /** Party per bend sinister. Divided in half reverse diagonally. */
  PARTY_PER_BEND_SINISTER,
  /** Party per chevron. Lower wedge. */
  PARTY_PER_CHEVRON,
  /** Party per cross. */
  PARTY_PER_CROSS,
  /** Party per fess. Divided in half horizontally. */
  PARTY_PER_FESS,
  /** Party per pale. Divided in half vertical. */
  PARTY_PER_PALE,
  /** Party per saltire. */
  PARTY_PER_SALTIRE,
  /** Pile. Wedge at the top. */
  PILE,
  /** Rustre. Shield in a shield in a shield. */
  RUSTRE,
  /** Saltire. Diagonal cross. */
  SALTIRE,
}
